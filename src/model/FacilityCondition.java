/**
 * Enumeration containing all types of the conditions of a FlatFacility.
 *
 * @author jorge
 */
public enum FacilityCondition {

    /**
     * Condition is new
     */
    NEW,
    /**
     * Condition is broken
     */
    BROKEN,
    /**
     * Condition is working
     */
    WORKING
}
